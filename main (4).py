import math

def RingS(R1, R2):
    """Функція для обчислення площі кільця між двома колами"""
    if R1 <= R2:
        raise ValueError("Радіус зовнішнього кола (R1) повинен бути більшим за радіус внутрішнього кола (R2).")
    
    # Використовуємо значення π = 3.14
    pi = 3.14
    
    # Обчислюємо площі двох кіл та їх різницю
    area_ring = pi * (R1**2 - R2**2)
    
    return area_ring

# Приклад використання функції для трьох кілець:
R1_1, R2_1 = 8, 4
R1_2, R2_2 = 14, 9
R1_3, R2_3 = 22, 14

print(f"Площа першого кільця: {RingS(R1_1, R2_1)}")
print(f"Площа другого кільця: {RingS(R1_2, R2_2)}")
print(f"Площа третього кільця: {RingS(R1_3, R2_3)}")